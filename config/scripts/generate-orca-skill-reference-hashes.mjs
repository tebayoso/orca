#!/usr/bin/env node
/**
 * Writes sha256 reference hashes for Orca-managed agent skills into shared TS.
 *
 * Why: freshness checks must not ship or package the skills/ tree into the app.
 * Hashes are computed from the repo skills/ source of truth at generate time.
 * Only managed skill names (those with in-app update rails) are hashed.
 *
 * Usage:
 *   node config/scripts/generate-orca-skill-reference-hashes.mjs --write
 *   node config/scripts/generate-orca-skill-reference-hashes.mjs --check
 */
import { createHash } from 'node:crypto'
import { existsSync, readFileSync, readdirSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'

const MAX_SKILL_MARKDOWN_BYTES = 256 * 1024
const ROOT = join(import.meta.dirname, '../..')
const SKILLS_ROOT = join(ROOT, 'skills')
const OUT_PATH = join(ROOT, 'src/shared/orca-skill-reference-hashes.generated.ts')

// Keep in sync with ORCA_MANAGED_SKILLS in src/shared/orca-managed-skills.ts
const MANAGED_SKILL_NAMES = ['orca-cli', 'orchestration', 'computer-use', 'orca-per-workspace-env']

function normalizeSkillMarkdownForHash(content) {
  return content.replace(/^\uFEFF/, '').replace(/\r\n/g, '\n')
}

function hashSkillMarkdown(content) {
  return createHash('sha256').update(normalizeSkillMarkdownForHash(content), 'utf8').digest('hex')
}

function buildSource() {
  if (!existsSync(SKILLS_ROOT)) {
    throw new Error(`skills/ not found at ${SKILLS_ROOT}`)
  }

  /** @type {Record<string, string>} */
  const hashes = {}
  for (const skillName of MANAGED_SKILL_NAMES) {
    const skillFilePath = join(SKILLS_ROOT, skillName, 'SKILL.md')
    if (!existsSync(skillFilePath)) {
      throw new Error(`Missing managed skill file: ${skillFilePath}`)
    }
    const content = readFileSync(skillFilePath)
    if (content.byteLength > MAX_SKILL_MARKDOWN_BYTES) {
      throw new Error(`Skill file exceeds ${MAX_SKILL_MARKDOWN_BYTES} bytes: ${skillFilePath}`)
    }
    hashes[skillName] = hashSkillMarkdown(content.toString('utf8'))
  }

  // Surface unexpected skills/ dirs without failing — never treat them as fixtures to ship.
  for (const entry of readdirSync(SKILLS_ROOT, { withFileTypes: true })) {
    if (!entry.isDirectory()) {
      continue
    }
    if (!MANAGED_SKILL_NAMES.includes(entry.name)) {
      console.warn(
        `[generate-orca-skill-reference-hashes] skipping non-managed skill dir: ${entry.name}`
      )
    }
  }

  return `/* eslint-disable */
// AUTO-GENERATED by config/scripts/generate-orca-skill-reference-hashes.mjs
// Do not edit by hand. Re-run the script after changing managed skills/*/SKILL.md.
// Why: package freshness without shipping the skills/ tree into the app bundle.

export const ORCA_SKILL_REFERENCE_HASHES: Readonly<Record<string, string>> = Object.freeze(${JSON.stringify(
    hashes,
    null,
    2
  )})
`
}

function main() {
  const args = new Set(process.argv.slice(2))
  const write = args.has('--write')
  const check = args.has('--check')
  if (write === check) {
    throw new Error('Pass exactly one of --write or --check')
  }

  const next = buildSource()
  if (write) {
    writeFileSync(OUT_PATH, next, 'utf8')
    console.log(`Wrote ${OUT_PATH} (${MANAGED_SKILL_NAMES.length} managed skills)`)
    return
  }

  if (!existsSync(OUT_PATH)) {
    throw new Error(`Missing ${OUT_PATH}. Run: pnpm run generate:orca-skill-reference-hashes`)
  }
  const current = readFileSync(OUT_PATH, 'utf8')
  if (current !== next) {
    throw new Error(
      `Stale orca skill reference hashes at ${OUT_PATH}. Run: pnpm run generate:orca-skill-reference-hashes`
    )
  }
  console.log('orca skill reference hashes are up to date')
}

main()
